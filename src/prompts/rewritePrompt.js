export function buildRewritePrompt({ articleText, sourceUrl }) {
  const urlNote = sourceUrl
    ? `Джерело для контексту: ${sourceUrl}\n`
    : "Джерело для контексту: текст надіслав адміністратор напряму.\n";

  return `
Ти - редактор новинного Telegram-каналу.
Потрібно зробити акуратний рерайт новини українською мовою.

Вимоги:
- Зберігай факти та суть, не вигадуй подробиць.
- Пиши жваво, але без клікбейту та сенсаційності.
- Напиши короткий і виразний заголовок у першому рядку.
- Після заголовка залиш порожній рядок.
- Основний текст розбий на 2-4 абзаци.
- Якщо в оригінальному тексті є дати, цифри, назви компаній, імена та назви місць, не спотворюй їх.
- Не додавай таких фраз, як «ось рерайт», «готово», «за даними джерела»".
- Не використовуй розмітку Markdown.

${urlNote}
Оригінальний текст новини:
"""
${articleText}
"""
`.trim();
}
