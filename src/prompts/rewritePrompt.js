export function buildRewritePrompt({ articleText, sourceUrl }) {
  const urlNote = sourceUrl
    ? `Источник: ${sourceUrl}\n`
    : "Источник: текст прислал администратор напрямую.\n";

  return `
Ты редактор новостного Telegram-канала.
Нужно сделать аккуратный рерайт новости на русском языке.

Требования:
- Сохраняй факты и смысл, не выдумывай детали.
- Пиши живо, но без кликбейта и желтизны.
- Сделай короткий сильный заголовок первой строкой.
- После заголовка оставь пустую строку.
- Основной текст сделай в 2-4 абзаца.
- Если в исходнике есть даты, цифры, названия компаний, имена и места, не искажай их.
- Не добавляй фразы вроде "вот рерайт", "готово", "по данным источника".
- Не используй markdown-разметку.
- В конце добавь отдельной строкой: Источник: ${sourceUrl || "по сообщению редакции"}.

${urlNote}
Исходный текст новости:
"""
${articleText}
"""
`.trim();
}
